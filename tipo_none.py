"""
Tipo None
O tipo de dado "none" em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio, porém
falar que é tipo sem tipo é mais apropriado.

Obs: O tipo nome especificado com a primeira letra Maiúscula
Certo: None
Errado: none
 - Podemos utilizar "None" quando queriamos criar uma varíavel e inicializa-la com um tipo sem tipo, antes de recebermos
 um valor final.

 Obs: O tipo "None" sempre será considerado como "false"
"""

numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))
